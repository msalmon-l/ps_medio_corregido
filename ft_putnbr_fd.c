#include "push_swap.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	imp;

	if (n == -2147483648)
	{
		ft_putstr("-2");
		n = 147483648;
	}
	if (n < 0)
	{
		ft_putstr("-");
		n = n * -1;
	}
	if (n < 10)
	{
		imp = (n + '0');
		write(fd, &imp, 1);
	}
	else
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
}
